/*
* Elprograma escriu els primers numeros naturals que siguin parells fins el 10.
*/

public class PrimersParells {
    public static void main(String[] args) {
    
        for (int numero = 2;
             numero <= 10;
             numero = numero + 2) {      
             System.out.println(numero);
            
        }
    }

}
